async function main() {
  for (const e of ENTRIES) {
    const slug = topicToSlug(e.topic);
    await prisma.entry.deleteMany({ where: { slug } });
    await prisma.entry.create({
      data: {
        slug,
        topic: e.topic,
        topicKind: e.topicKind,
        crux: e.crux,
        ...PROVENANCE,
        lenses: {
          create: e.lenses.map((l, i) => ({
            name: l.name,
            family: l.family,
            summary: l.summary,
            body: l.body,
            grounding: l.grounding,
            epistemicType: l.epistemicType,
            confidence: l.confidence ?? null,
            order: i,
            profileKey: l.profileKey ?? null,
            profileVersion: l.profileVersion ?? null,
          })),
        },
      },
    });
    console.log(`seeded (needs_review): ${e.topic} (${e.lenses.length} עדשות)`);
  }
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
